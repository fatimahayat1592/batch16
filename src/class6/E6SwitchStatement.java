package class6;

public class E6SwitchStatement {
    public static void main(String[] args) {
        String name="Jacob";
        switch(name){
            case "Vlad":
                System.out.println("Virginia");
                break;
            case "Jacob":
                System.out.println("Chicago");
                break;
            case "Axel":
                System.out.println("Atlanta");
        }
    }
}
