package class7;

public class E4WhileLoops {
    public static void main(String[] args) {
        int start=1;
        while(start<=5){
            System.out.println(start);
            start++;
        }
        int num1=3;
        while(num1<=11){
            System.out.println(num1);
            num1++;
        }
    }
}
